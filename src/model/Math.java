package model;

public class Math {

	static public double sum(double a, double b) {
		return a+b;
	}
	static public double sub(double a, double b) {
		return a-b;
	}
	static public double times(double a, double b) {
		return a*b;
	}
	static public double div(double a, double b) {
		return a/b;
	}
}
